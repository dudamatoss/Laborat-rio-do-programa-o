

class Calculadora
{
    public double Somar(double x, double y)
    {
      double resultado = x + y ; 
      return resultado;
    }
    public double Subtrair (double x, double y)
    {
        return x - y;
    }
    public double Multiplicacao (double x, double y)
    {
        return x * y;
    }
    public double Dividir(double x, double y)
    {
        return x / y;
    }

}